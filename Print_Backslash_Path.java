public class Print_Backslash_Path {
    public static void main(String[] args) {

        // IN JAVA , THE BLACKSLASH (\) IS A SPECIAL CHARACTER.
        // IF YOU WANT TO PRINT A LITERAL BACKSLASH :::
        // YOU NEED TO ESCAPE IT WITH ANOTHER BACKSLASH.
        // DOUBLING THE BACKSLASHES IS NECESSARY.
        System.out.println("C:\\Java\\bin");
    }
}
